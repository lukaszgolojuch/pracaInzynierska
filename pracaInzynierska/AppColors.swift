import SwiftUI

struct AppColors {
    
    var darkGrey: Color {
        get {
            return Color(red: 44/255, green: 44/255, blue: 44/255)
        }
    }
    
    var lightGrey: Color {
        get {
            return Color(red: 152/255, green: 152/255, blue: 152/255)
        }
    }
    
    var gold: Color {
        get {
            return Color(red: 255/255, green: 215/255, blue: 0/255)
        }
    }
    
    
}
